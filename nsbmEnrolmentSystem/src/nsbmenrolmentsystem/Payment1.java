    void display(){
       try{
            Class.forName("com.mysql.jdbc.Driver"); //load and register jdbc driver
            conn=(Connection)DriverManager.getConnection(url,username,password);
            String query="select subName,payment from subjects";
            pst=conn.prepareStatement(query);
            ResultSet res=pst.executeQuery();
            double x1=0,x2=0,x3=0,x4=0,x5=0,x6=0,x7=0;
            while(res.next()){
                String sn=res.getString("subName");
                String pay=res.getString("payment");
                if(sn.equals(ls1.getText())){
                    l1.setText(pay);
                    x1=Double.parseDouble(pay);
                }else if(sn.equals(ls2.getText())){
                    l2.setText(pay);
                    x2=Double.parseDouble(pay);
                }else if(sn.equals(ls3.getText())){
                    l3.setText(pay);
                    x3=Double.parseDouble(pay);    
                }else if(sn.equals(ts1.getText())){
                    l4.setText(pay);
                    x4=Double.parseDouble(pay);
                }else if(sn.equals(ts2.getText())){
                    l5.setText(pay);
                    x5=Double.parseDouble(pay);
                }else if(sn.equals(ts3.getText())){
                    l6.setText(pay);
                    x6=Double.parseDouble(pay);
                }else if(sn.equals(ts4.getText())){
                    l7.setText(pay);
                    x7=Double.parseDouble(pay);
                }
                double tot=x1+x2+x3+x4+x5+x6+x7;
                l8.setText(String.valueOf(tot));
                financeworks fw=new financeworks();
                fw.setName(tname.getText());
                fw.setPayment(tot);
                boolean x=jdb.paymentcost(fw);
                System.out.println(x);
            }
       }catch(Exception e){
           
       } 
    }
